package ejercicios;

/**Este programa utiliza el ciclo while para contar numeros que esten en el rango del 1 a 10
 *
 * @author Acer
 */
public class ejercicio6 {
    public static void main(String[] args) {
        int i = 0;
        
        while(i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}
