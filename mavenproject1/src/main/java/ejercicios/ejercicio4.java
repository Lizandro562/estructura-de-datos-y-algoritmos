package ejercicios;

/**Este programa utiliza el bucle for para crear números decrecientes que estan en el rango del 10 al 1
 *
 * @author Acer
 */
public class ejercicio4 {
    public static void main(String[] args) {
        for (int i = 10; i >= 1;i--){//se crea un números decrecientes con el bucle for 
            System.out.println(i);//se inprime números decrecientes del 10 al 1
        }
    }
}
